"""
fx_catalog.py — Forenstix-2.0 tool registry

Defines every tool available through the plugin.  Tools are grouped into
categories.  Each :class:`Tool` carries enough metadata for fx_run.py to
execute it in the right backend and for Claude to pick the right tool for a
given task.

Reliability legend
  green  — plug-and-play; runs in Docker/native without fuss
  yellow — depends on backend, hardware, or sudo config

Flag vocabulary
  sudo        needs elevated privileges on native/WSL
  interactive requires a TTY / interactive session
  long        typically runs for minutes
  hw          requires physical hardware (WiFi adapter, Bluetooth, etc.)
  gui         launches a graphical UI
  archived    upstream project is no longer maintained
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Dict, List, Optional


@dataclass
class Tool:
    id: str                             # unique snake_case identifier
    name: str                           # human display name
    url: str                            # upstream repo / website
    category: str                       # category key
    description: str                    # one-line description
    install_cmd: str                    # native/WSL install command
    run_cmd: str                        # run template; {args} = user-supplied args
    docker_image: str = ""              # preferred Docker image; "" = kali fallback
    flags: List[str] = field(default_factory=list)
    reliability: str = "yellow"         # "green" | "yellow"
    aliases: List[str] = field(default_factory=list)


