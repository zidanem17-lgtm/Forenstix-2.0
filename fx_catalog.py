def by_id() -> Dict[str, Tool]:
    return {t.id: t for t in TOOLS}


def by_category(cat: str) -> List[Tool]:
    return [t for t in TOOLS if t.category == cat]


def search(query: str) -> List[Tool]:
    """Case-insensitive search across id, name, description, aliases."""
    q = query.lower()
    results = []
    for t in TOOLS:
        if (
            q in t.id.lower()
            or q in t.name.lower()
            or q in t.description.lower()
            or any(q in a.lower() for a in t.aliases)
        ):
            results.append(t)
    return results


def stats() -> Dict[str, int]:
    """Return per-category tool counts."""
    counts: Dict[str, int] = {}
    for t in TOOLS:
        counts[t.category] = counts.get(t.category, 0) + 1
    return counts
